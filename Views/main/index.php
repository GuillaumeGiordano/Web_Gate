    <h3>A quoi sert l'Essentiel G2 ?</h3></br>

    <p><i>Vérifié le 23 juin 2020 - Direction de l'information légale et administrative (Premier ministre)</i></p>

    <p>Pour répondre à ses besoins en matière de travaux, de fournitures et de services,
        un acheteur public ou privé (État, collectivité territoriale, hôpital, etc.) doit
        conclure un marché public avec un opérateur économique. Les règles qui s'appliquent
        à ces contrats sont définies par la législation relative aux marchés publics.</p>

    <p>Un marché public est un contrat conclu à titre onéreux entre un acheteur public ou privé
        et un opérateur économique public ou privé. Ce marché doit répondre aux besoins de
        l'acheteur en matière de travaux, de fournitures ou de services.
    </p>
    <p>Les marchés dont la valeur est égale ou supérieure à 40 000 €HT: HT : Hors taxesHT :
        Hors taxes doivent obligatoirement être passés par voie électronique. Ils peuvent
        être passés sur la plateforme des achats de l'État (PLACE) ou les plateformes des
        collectivités locales et établissements publics.
    </p>
